package Proyecto;
public class Cancion {
    //Atributos
    public String nombre;
    public String duracion;
    public String compositor;
    
    //Constructores
    public Cancion(){}

    public Cancion(String nombre, String duracion, String compositor){
        this.nombre=nombre;
        this.duracion=duracion;
        this.compositor=compositor;
    }
}
